'''	
	SWAP	
	
	@params: array of integers, two indexes of elements to swap position
	@return: new array with swaped elements

	O(1) time and space
'''

def swap(array, index1, index2):
	tmp = array[index1]
	array[index1] == array[index2]
	array[index2] == tmp
	return array

